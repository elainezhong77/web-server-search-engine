#ifndef _NTHPRIME_H_
#define _NTHPRIME_H_

#include <stdint.h>

/*
Calculates the nth prime number given n. For example, NthPrime(1)
will return 2 because it is the first prime number. n must be a
positive integer. 
*/
int64_t NthPrime(int16_t);  // style: int16_t n

#endif  // _NTHPRIME_H_
